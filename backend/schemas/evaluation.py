"""Evaluation Schemas and Enums.

Defines Pydantic data schemas representing evaluation task types,
parameter settings configs, computed metric summaries, and runtime results.
"""

from datetime import datetime
from enum import Enum
from typing import Any, Dict, Optional
from pydantic import BaseModel, Field


class TaskType(str, Enum):
    """Supported task types for model evaluation loops."""
    GENERATION = "generation"
    CLASSIFICATION = "classification"


class EvaluationConfig(BaseModel):
    """Configuration parameters representing evaluation task runs parameters."""

    evaluation_id: str = Field(
        ..., 
        description="Unique identifier key representing this evaluation task"
    )
    model_id: str = Field(
        ..., 
        description="Target registered model checkpoint UUID mapping to registry"
    )
    dataset_id: str = Field(
        ..., 
        description="Target registered dataset ID matching dataset repository"
    )
    task_type: TaskType = Field(
        default=TaskType.GENERATION,
        description="Task evaluation criteria (generation or classification)"
    )
    batch_size: int = Field(
        4, 
        ge=1, 
        description="Step batch size per inference pass"
    )
    max_new_tokens: int = Field(
        128, 
        ge=1, 
        description="Maximum target generated tokens count limit"
    )
    temperature: float = Field(
        0.0, 
        ge=0.0, 
        le=2.0, 
        description="Generation temperature control parameter"
    )
    top_p: float = Field(
        1.0, 
        ge=0.0, 
        le=1.0, 
        description="Top-p sampling generation threshold"
    )
    random_seed: int = Field(
        42, 
        description="Random seed for reproducibility"
    )


class MetricSummary(BaseModel):
    """Calculated metric float scores wrapper for output registries."""

    bleu: Optional[float] = Field(None, description="BLEU generation similarity score")
    rouge_l: Optional[float] = Field(None, description="ROUGE-L generation LCS overlap score")
    bert_score: Optional[float] = Field(None, description="BERTScore generation mock value")
    accuracy: Optional[float] = Field(None, description="Classification accuracy ratio")
    precision: Optional[float] = Field(None, description="Classification precision score")
    recall: Optional[float] = Field(None, description="Classification recall score")
    f1_score: Optional[float] = Field(None, description="Classification F1-score metric")


class EvaluationResult(BaseModel):
    """Execution status output metrics generated by an evaluation run."""

    evaluation_id: Optional[str] = Field(None, description="Matches configuration ID")
    model_id: Optional[str] = Field(None, description="Evaluated model ID")
    dataset_id: Optional[str] = Field(None, description="Target dataset ID")
    task_type: Optional[TaskType] = Field(None, description="Task category type")
    metrics: Optional[MetricSummary] = Field(None, description="Calculated evaluation metrics summary")
    runtime_seconds: float = Field(0.0, description="Total execution duration in seconds")
    report_path: Optional[str] = Field(None, description="File path directory storing structured reports files")
    created_at: datetime = Field(default_factory=datetime.utcnow, description="Creation timestamp")

    # Legacy fields for backward compatibility with Phase 1 tests
    eval_id: Optional[str] = Field(None, description="Legacy eval ID key")
    model_name: Optional[str] = Field(None, description="Legacy model name key")
    dataset_name: Optional[str] = Field(None, description="Legacy dataset name key")
    rouge_scores: Optional[Dict[str, Any]] = Field(default_factory=dict, description="Legacy ROUGE dictionary")
    bertscore: Optional[float] = Field(None, description="Legacy BERTScore key")
    exact_match: Optional[float] = Field(None, description="Legacy exact match ratio")
    latency_ms: Optional[float] = Field(None, description="Legacy latency stats")
    throughput_tokens_per_sec: Optional[float] = Field(None, description="Legacy throughput speed stats")
    llm_judge_score: Optional[float] = Field(None, description="Legacy LLM judge score")
    cost_estimation_usd: Optional[float] = Field(None, description="Legacy cost stats")

    def __init__(self, **data: Any) -> None:
        """Synchronizes legacy keys with new evaluation pipeline keys."""
        if "eval_id" in data and "evaluation_id" not in data:
            data["evaluation_id"] = data["eval_id"]
        elif "evaluation_id" in data and "eval_id" not in data:
            data["eval_id"] = data["evaluation_id"]
            
        super().__init__(**data)

